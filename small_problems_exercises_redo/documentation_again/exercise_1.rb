# Locate the ruby documentation for methods File::path and File#path for version 2.7.8 of Ruby (newer versions of Ruby have changed the implementation of these methods, so be sure to follow the link provided). How are they different?

# https://ruby-doc.org/2.7.8/File.html#method-c-path 


# File::path is a class method, returns the string representation of the path

# File#path is an instance method, returns the pathname used to create file as a string.

# Class methods are called on the class, while instance methods are called on objects.

