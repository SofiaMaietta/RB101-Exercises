# Starting with this string:

munsters_description = "The Munsters are creepy in a good way."

# Convert the string in the following ways (code will be executed on original munsters_description above):

# "tHE mUNSTERS ARE CREEPY IN A GOOD WAY."

puts munsters_description.swapcase! # upcases each lowercase character and downcases each uppercase character


# "The munsters are creepy in a good way."

puts munsters_description.capitalize! # capitalizes only the first character


# "the munsters are creepy in a good way."

puts munsters_description.downcase! # converts all characters to lowercase


# "THE MUNSTERS ARE CREEPY IN A GOOD WAY."

puts munsters_description.upcase! # converts all characters to uppercase